"""
Temel veri tipleri
tam sayı -> integer int()  85
ondalikli sayi -> float float() 1.77


"""
# aşağıda sayı veri tipi için örnekler verilmiştir
print(85)
print(1.77)
#bir verinin tipini öğrenmek için type() fonk kullanılır
# type(x) => x'in veri tipini verir veya döndürür
print(type(85))
print(type(1.77))

print(85.0)
print(type(85.0))